#Crie um algoritmo que leia um número real e mostre na tela o seu dobro e a sua terça parte.

num = float(input("num: ")) #exibe uma mensagem, entra com o dado e salva na variavel tipo float

print(f"O dobro de {num} é: {num*2}") #chama a variavel num e multiplica ela por 2, exibe a mensagem
print(f"A terça parte de {num} é: {num/3}") #chama a variavel num e divide por 3, exibe a mensagem